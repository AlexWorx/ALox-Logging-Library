//! @cond NO_DOX
namespace aworx { namespace lib { namespace strings
{
    // Implementation of T_Apply for enum type Scope
    template<> struct       T_Apply<Scope> : public std::true_type
    {
        static inline integer Apply( AString& target, const Scope src )
        {
            Scope scope= src;
            int pathLevel= EnumValue( scope - Scope::Path );
            if(pathLevel > 0 )
                scope= Scope::Path;

            auto& enumMetaData= *EnumMetaData<Scope>::GetSingleton();
            enumMetaData.CheckLoad();
            target << "Scope::" << std::get<1>(*enumMetaData.Get( scope ) );

            if( pathLevel > 0 )
                target << '+' << pathLevel;

            return 1;
        }
    };

    // Implementation of T_Apply for pairs of Verbosity and Priorities
    template<> struct       T_Apply<std::pair<Verbosity, Priorities>> : public std::true_type
    {
        static inline integer Apply( AString& target, std::pair<Verbosity, Priorities>& src )
        {
            target._( Format::Field( src.first, 7, Alignment::Left) );
            target._( '(' )._( src.second );
            target.InsertAt( ")", target.LastIndexOfAny<Inclusion::Exclude>( DefaultWhitespaces )  + 1 );

            return 1;
        }
    };


}}}

