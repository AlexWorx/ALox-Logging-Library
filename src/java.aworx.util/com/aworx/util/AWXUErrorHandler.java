package com.aworx.util;

/**************************************************************************************************
 * Interface for AWXU error handler.
 **************************************************************************************************/
public interface AWXUErrorHandler
{
    /**************************************************************************************************
     * Report an AWXU Error.
     *
     * @param msg   The error message.
     **************************************************************************************************/
	public void error  ( Object msg );

    /**************************************************************************************************
     * Report an AWXU Error.
     *
     * @param msg   The error message.
     **************************************************************************************************/
	public void warning( Object msg );
}
